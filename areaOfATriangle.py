# This program calculates the area of a Triangle, accepting the base and height as input from the user

# This two lines of code  below takes the input of the user
baseOfTriangle = float(input("Please Enter The Base Of The Triangle: "))
heightOfTriangle = float(input("Please Enter The Height Of The Triangle: "))

# The code below computes the area of the triangle
areaOfTriangle = 0.5 * baseOfTriangle * heightOfTriangle

# The code below produces the result of the computation to the user
print('The Result Is: ', areaOfTriangle)
